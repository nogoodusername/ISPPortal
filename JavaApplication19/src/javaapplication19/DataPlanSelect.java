    private void select_planActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_select_planActionPerformed
        String plan = (String) select_plan.getSelectedItem();
        
        Connection con = DBConnect.ConnectDB();
        
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM QQ.DATAPLAN WHERE NAME = '"+plan+"'");
            rs.next();
            text_down.setText(rs.getString("DOWNLOAD"));
            text_up.setText(rs.getString("UPLOAD"));
            text_val.setText(rs.getString("VALIDITY"));
            text_tar.setText(rs.getString("TARIFF"));
            text_pid.setText(Integer.toString(rs.getInt("PLANID")));         
        }   catch(SQLException e)   {
            System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_select_planActionPerformed

    private void selectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectActionPerformed
        Connection con = DBConnect.ConnectDB();
        String w = (String) select_plan.getSelectedItem();
        try {
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT PLANID FROM QQ.DATAPLAN WHERE NAME = '"+w+"'");
            rs.next();
            int i = rs.getInt("PLANID");
            rs = st.executeQuery("SELECT PID FROM QQ.CUST WHERE CUSTID = "+cid);
            rs.next();
            rs.updateInt("PID", i);
            rs.updateRow();
        }   catch(SQLException e)   {
            System.out.println(e.getMessage());
        }
        JOptionPane.showMessageDialog(null, "Done!!");
        setVisible(false);
        Start a = new Start();
        a.setVisible(true);
    }//GEN-LAST:event_selectActionPerformed

