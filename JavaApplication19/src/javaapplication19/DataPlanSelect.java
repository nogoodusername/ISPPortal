    public Vector getPlan()    {
        Vector planv = new Vector();
        Connection con = DBConnect.ConnectDB();
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT NAME FROM QQ.DATAPLAN");
            while(rs.next())    {
                String plan = rs.getString("NAME");
                planv.addElement(plan);
            }
        }   catch(SQLException e)   {
            System.out.println(e.getMessage());
        }
        return planv;
    }
    
